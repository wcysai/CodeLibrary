ull nim_add(ull x,ull y)
{
    return x^y;
}

ull nim_mult_pow(ull x,ull y)
{
    if(x<64)
        return sg[x][y];
    ull a=0;
    for(;;a++)
        if(a==5||(x>=(1ULL<<(1ULL<<a))&&x<(1ULL<<(1ULL<<(a+1)))))
            break;
    ull m=1ULL<<(1ULL<<a);
    ull p=x/m,s=y/m,t=y%m;
    ull d1=nim_mult_pow(p,s);
    ull d2=nim_mult_pow(p,t);
    return (m*(d1^d2))^nim_mult_pow(m/2,d1);
}

ull nim_mult(ull x,ull y)
{
    if(x<y)
        return nim_mult(y,x);
    if(x<64)
        return sg[x][y];
    ull a=0;
    for(;;a++)
        if(a==5||(x>=(1ULL<<(1ULL<<a))&&x<(1ULL<<(1ULL<<(a+1)))))
            break;
    ull m=1ULL<<(1ULL<<a);
    ull p=x/m,q=x%m,s=y/m,t=y%m;
    ull c1=nim_mult(p,s);
    ull c2=nim_mult(p,t)^nim_mult(q,s);
    ull c3=nim_mult(q,t);
    return (m*(c1^c2))^c3^nim_mult_pow(m/2,c1);
}

ull nim_pow(ull a,ull i)
{
    ull s=1;
    while(i)
    {
        if(i&1) s=nim_mult(s,a);
        a=nim_mult(a,a);
        i>>=1ULL;
    }
    return s;
}

ull get_inv(ull a)
{
    return nim_pow(a,C);
}

ull pre[70];
void precompute(ull t)
{
    for(int i=0;i<64;i++)
    {
        pre[i]=0;
        for(int j=0;j<64;j++) if(t&(1ULL<<j)) pre[i]=nim_add(pre[i],p[i][j]);
    }
}
ull get_ans(ull x)
{
    ull ans=0;
    for(int i=0;i<64;i++)
        if(x&(1ULL<<i)) ans=nim_add(ans,pre[i]);
    return ans;
}

ull get_det(mat A)
{
	int n=(int)A.size();
    ull det=1;
    for(int i=0;i<n;i++)
    {
        int k=i;
        for(int j=i+1;j<n;j++) if(A[j][i]>A[k][i]) k=j;
        if(A[k][i]==0) return 0;
        swap(A[i],A[k]);
        det=nim_mult(det,A[i][i]);
        ull inv=get_inv(A[i][i]);
        for(int j=i+1;j<n;j++) A[i][j]=nim_mult(A[i][j],inv);
        for(int j=0;j<n;j++)
        {
            if(j!=i&&A[j][i])
            {
                precompute(A[j][i]);
                for(int k=i+1;k<n;k++) A[j][k]=nim_add(A[j][k],get_ans(A[i][k]));
            }
        }
    }
    return det;
}
